namespace GraphShape.Algorithms.Layout
{
    /// <summary>
    /// Enumeration of possible layout directions.
    /// </summary>
    public enum LayoutDirection
    {
        /// <summary>
        /// Left to right direction.
        /// </summary>
        LeftToRight = 0,

        /// <summary>
        /// Top to bottom direction.
        /// </summary>
        TopToBottom = 1,

        /// <summary>
        /// Right to left direction.
        /// </summary>
        RightToLeft = 2,

        /// <summary>
        /// Bottom to top direction.
        /// </summary>
        BottomToTop = 3
    }
}